package huplay.demo.config;

public enum DataType
{
    // Boolean
    BOOL,

    // Unsigned byte
    U8,

    // Signed byte
    I8,

    // 16-bit signed integer
    I16,

    // 16-bit unsigned integer
    U16,

    // 16-bit float
    F16,

    // 16-bit brain float
    BF16,

    // 32-bit signed integer
    I32,

    // 32-bit unsigned integer
    U32,

    // 32-bit float
    F32,

    // 64-bit float
    F64,

    // 64-bit signed integer
    I64,

    // 64-bit unsigned integer
    U64
}
